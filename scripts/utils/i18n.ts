const I18N: Record<string, Translation> = {
  'en': en,
  'zh': zh,
  'zh-TW': zhTW,
  'ja-JP': ja,
  'ko-KR': ko,
  'th-TH': th,
  'vi-VN': vi,
  'hi-IN': hi,
  'es-ES': es,
  'es-419': es, // Fallback to es for now
  'de-DE': de,
  'fr-FR': fr,
  'it-IT': it,
  'pt-BR': pt,
  'pt-PT': pt, // Fallback to pt-BR or similar if not distinct enough yet
  'tr-TR': tr,
};

export function t(key: keyof Translation, locale: string): string {
  // Try specific locale match first
  if (I18N[locale] && I18N[locale][key]) {
    return I18N[locale][key];
  }

  // Fallback logic
  if (locale === 'es-419') return I18N['es-ES'][key] || en[key];
  if (locale === 'pt-PT') return I18N['pt-BR'][key] || en[key];
  
  // Default fallback to English
  return en[key] || key;
}

